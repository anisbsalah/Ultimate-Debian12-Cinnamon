echo
tput setaf 6
echo "######################################################################################################"
echo "PLYMOUTH"
echo "######################################################################################################"
tput sgr0

tput setaf 3
echo "######################################################################################################"
echo "################# Installing plymouth themes..."
echo "######################################################################################################"
tput sgr0
echo

sudo apt install -y plymouth-themes

sudo plymouth-set-default-theme -R spinner

FIND='GRUB_CMDLINE_LINUX_DEFAULT="quiet'
REPLACE='GRUB_CMDLINE_LINUX_DEFAULT="quiet loglevel=0 splash'
sudo sed -i "s/${FIND}/${REPLACE}/" /etc/default/grub

sudo grub-mkconfig -o /boot/grub/grub.cfg

echo
tput setaf 2
echo "######################################################################################################"
echo "################# Plymouth themes have been installed"
echo "######################################################################################################"
tput sgr0
echo
