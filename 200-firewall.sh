echo
tput setaf 6
echo "######################################################################################################"
echo "FIREWALL"
echo "######################################################################################################"
tput sgr0
echo
echo "Select the firewall program to install:"
echo "0) Do nothing"
echo "1) Firewalld"
echo "2) UFW"
echo
read -erp "Enter a number (0/1/2): " CHOICE
echo

case ${CHOICE} in

0)
	tput setaf 13
	echo "######################################################################################################"
	echo "################# We did nothing as per your request"
	echo "######################################################################################################"
	tput sgr0
	echo
	;;
1)
	tput setaf 3
	echo "######################################################################################################"
	echo "################# Firewalld to be installed..."
	echo "######################################################################################################"
	tput sgr0
	echo

	sudo apt install -y firewalld firewall-config firewall-applet
	sudo systemctl enable --now firewalld.service

	echo
	tput setaf 2
	echo "######################################################################################################"
	echo "################# Firewalld has been installed"
	echo "######################################################################################################"
	tput sgr0
	echo
	;;
2)
	tput setaf 3
	echo "######################################################################################################"
	echo "################# UFW to be installed..."
	echo "######################################################################################################"
	tput sgr0
	echo

	sudo apt install -y ufw gufw
	sudo systemctl enable --now ufw.service
	sudo ufw enable

	echo
	tput setaf 2
	echo "######################################################################################################"
	echo "################# UFW has been installed"
	echo "######################################################################################################"
	tput sgr0
	echo
	;;
*)
	tput setaf 9
	echo "######################################################################################################"
	echo "################# Choose the correct number"
	echo "######################################################################################################"
	tput sgr0
	echo
	;;
esac
